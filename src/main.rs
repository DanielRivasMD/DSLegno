fn main() {
	launch(App);
}

fn StoryListing() -> Element {
	let title = "title";
	let by = "author";
	let score = 0;
	let time = chrono::Utc::now();
	let comments = "comments";

	rsx! {
		div { padding: "0.5rem", position: "relative",
			"{title} by {by} ({score}) {time} {comments}"
		}
	}
}

pub fn App() -> Element {
	rsx! { StoryListing {} }
}

