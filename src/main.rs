fn main() {
	launch(App);
}

pub fn App() -> Element {
	let title = "title";
	let by = "author";
	let score = 0;
	let time = chrono::Utc::now();
	let comments = "comments";

	rsx! {"{title} by {by} ({score}) {time} {comments}"}
}

